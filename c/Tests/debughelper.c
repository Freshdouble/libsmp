static bool SequenceEqual(uint8_t *seq1, uint8_t *seq2, uint32_t length)
{
    while (length > 0)
    {
        length--;
        if (*seq1 != *seq2)
        {
            return false;
        }
        seq1++;
        seq2++;
    }
    return true;
}

static void printPacket(uint8_t *data, uint32_t length)
{
    for (int i = 0; i < length; i++)
    {
        if (i > 0)
        {
            printf(",");
        }
        else
        {
            printf("\t");
        }

        printf("0x%02X", data[i]);
    }
    printf("\n");
}

